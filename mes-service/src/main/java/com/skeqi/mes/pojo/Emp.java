package com.skeqi.mes.pojo;

import java.util.Date;

public class Emp {

	private Integer empno;//NUMBER(4,0),
	private String ename;//VARCHAR2(10),
	private String job;//VARCHAR2(9),
	private Integer mgr;//NUMBER(4,0),
	private Date hiredate;//DATE,
	private Integer sal;//NUMBER(7,2),
	private Integer comm;//NUMBER(7,2),
	private Integer deptno;//NUMBER(2,0),
	public Integer getEmpno() {
		return empno;
	}
	public void setEmpno(Integer empno) {
		this.empno = empno;
	}
	public String getEname() {
		return ename;
	}
	public void setEname(String ename) {
		this.ename = ename;
	}
	public String getJob() {
		return job;
	}
	public void setJob(String job) {
		this.job = job;
	}
	public Integer getMgr() {
		return mgr;
	}
	public void setMgr(Integer mgr) {
		this.mgr = mgr;
	}
	public Date getHiredate() {
		return hiredate;
	}
	public void setHiredate(Date hiredate) {
		this.hiredate = hiredate;
	}
	public Integer getSal() {
		return sal;
	}
	public void setSal(Integer sal) {
		this.sal = sal;
	}
	public Integer getComm() {
		return comm;
	}
	public void setComm(Integer comm) {
		this.comm = comm;
	}
	public Integer getDeptno() {
		return deptno;
	}
	public void setDeptno(Integer deptno) {
		this.deptno = deptno;
	}


}
